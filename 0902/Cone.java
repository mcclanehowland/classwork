public class Cone
{
	public static void main(String[] args)
	{
		double radius = 3;
		double pi = 3.14;
		double height = 6;
		double volume;
		volume = pi * radius * radius * height;
		System.out.println("radius = " + radius);
		System.out.println("height = " + height);
		System.out.println("pi = " + pi);
		System.out.println("volume = " + volume);
		
		
	}
}